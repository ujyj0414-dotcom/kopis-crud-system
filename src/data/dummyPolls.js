// --- 헬퍼 함수 (데이터 생성 및 셔플) ---
const diverseRandom = (min, max) => {
  return Math.floor(min + Math.random() * (max - min + 1));
};

const shuffleArray = (array) => {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
};

const createTieredAndShuffledData = (tiers, generatorFn) => {
  const allPolls = [];
  tiers.forEach((tier) => {
    for (let i = 0; i < tier.count; i++) {
      allPolls.push(generatorFn(tier.min, tier.max));
    }
  });
  shuffleArray(allPolls);
  return allPolls;
};

// --- 데이터 생성 로직 (이전과 동일) ---
const generateHotPollFromRange = (min, max) => {
  const likes = diverseRandom(min, max);
  const dislikeRatio = 0.1 + Math.random() * 0.39;
  const dislikes = Math.floor(likes * dislikeRatio);
  const comments = Math.floor(
    (likes + dislikes) * (Math.random() * 0.1 + 0.05)
  );
  return { likes, dislikes, comments };
};
