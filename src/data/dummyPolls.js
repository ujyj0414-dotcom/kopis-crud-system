const generateHalfHalfPollFromRange = (min, max) => {
  const base = diverseRandom(min, max);
  const diff = base * 0.1;
  let likes = Math.floor(base + (Math.random() * diff * 2 - diff));
  let dislikes = Math.floor(base + (Math.random() * diff * 2 - diff));
  likes = Math.max(min, Math.min(max, likes));
  dislikes = Math.max(min, Math.min(max, dislikes));
  const comments = Math.floor(
    (likes + dislikes) * (Math.random() * 0.1 + 0.05)
  );
  return { likes, dislikes, comments };
};
